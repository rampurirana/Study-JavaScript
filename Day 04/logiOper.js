// Logical operator : It is used for combine multiple conditions, and return boolean value ( true and false )

// AND (&&)
let a = 10;
cond1 = a==10;
cond2 = a>5;
console.log(cond1 && cond2);

// OR (||)
let b = 10;
cond3 = b==10;
cond4 = b>10;
console.log(cond3 || cond4);

// NOT (!)
let c = 10;
cond5 = c==10;
