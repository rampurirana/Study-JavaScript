// Comparison Operator : Use to compare two values
let a = 10;
let b = 20;

console.log("a =", a,"b =", b);
console.log("a == b :", a==b); // Equal to ( == )
console.log("a === b :", a===b); // Strict equal to ( === ) Check data types also
console.log("a != b :", a!=b); // Not equal to ( != )
console.log("a !== b :", a!==b); // Strict not equal to ( !== ) Check data type also
console.log("a > b :", a>b); // left is Greater than right ( > )
console.log("a < b :", a<b); // left is less than right ( < )
console.log("a >= b :", a>=b); // left is greater than or equal to the right ( >= )
console.log("a <= b :", a<=b); // left is less than or equal to the right ( <= )